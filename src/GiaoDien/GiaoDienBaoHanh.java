    private void showData() {
        new LoadData();
        int dem = 0;
        myTable.setRowCount(0);
        for (PhieuBaoHanh bh : Controller.controller.arrayListPhieuBaoHanh) {
            dem++;
            myTable.addRow(new Object[]{dem, bh.getMaPBH(), bh.getMaDH(), bh.getNgayBD(), bh.getNgayKT()});
        }

    }

    private void showData1() {
        new LoadData();
        int dem1 = 0;

        myTable1.setRowCount(0);
        for (CT_PhieuBaoHanh ctbh : Controller.controller.arrayListCT_PhieuBaoHanh) {
            dem1++;
            myTable1.addRow(new Object[]{dem1, ctbh.getMaPBH(), ctbh.getMaNV(), ctbh.getTrangThai()});
        }
    }

