    private void jTable1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MousePressed
        btnThem1.setEnabled(false);
        btnSua1.setEnabled(true);
        baoloi.setText("");// TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        txtPBH.setText((String) jTable1.getValueAt(row, 1));
        txtMDH.setText((String) jTable1.getValueAt(row, 2));
        String datebd = jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString();
        String datekt = jTable1.getValueAt(jTable1.getSelectedRow(), 4).toString();
        datebd = datebd.substring(0, 4) + "/" + datebd.substring(5, 7) + "/" + datebd.substring(8, 10);
        datekt = datekt.substring(0, 4) + "/" + datekt.substring(5, 7) + "/" + datekt.substring(8, 10);

        try {
            java.util.Date datetmp = new SimpleDateFormat("yyyy/MM/dd").parse(datebd);
            dateBD.setDate(datetmp);
            java.util.Date datetmp1 = new SimpleDateFormat("yyyy/MM/dd").parse(datekt);
            dateKT.setDate(datetmp1);
        } catch (ParseException ex) {
            Logger.getLogger(GiaoDienBaoHanh.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtPBH.setEnabled(false);

        if (evt.getClickCount() == 2) {
            jTable2.setDefaultEditor(Object.class, null);
            //jTable1.setSelectionBackground(Color.RED);
            jTable2.setSelectionMode(0);
            LayCT();
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jTable1MousePressed

